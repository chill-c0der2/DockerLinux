options=("Fedora" "Alpine" "Ubuntu" "Kali" "Debian") 
select opt in "${options[@]}" 
do
    case $opt in
        "Fedora")
            docker pull fedora
            docker run -it fedora
            ;;
        "Alpine")
            docker pull alpine
            docker run -it alpine
            ;;
            "Debian")
            docker pull debian
            docker run -it debian
            ;;
        "Ubuntu")
           docker pull ubuntu
           docker run -it ubuntu
            ;;
        "Kali")
            docker run -it kalilinux/kali-rolling
            ;;
        *) echo "Not A Option" clear echo "$options";;
        
    esac
done
